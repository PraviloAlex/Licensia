def norm_space(text: str) -> str:
    return re.sub(r"\s+", " ", (text or "").strip())


def correct_answer(q):
    return next(o for o in q["options"] if o["id"] == q["correctOptionId"])


def option_letter(q):
    return q["correctOptionId"].upper()


def is_true_false(q):
    answer = correct_answer(q)["text_es"].lower()
    return answer.startswith("verdadero") or answer.startswith("falso")


def is_generic(text: str) -> bool:
    text = text or ""
    generic_bits = [
        "соответствует правилу по теме",
        "в этой задаче нужно применить норму",
        "Сначала безопасность, потом скорость",
        "Сначала безопасность, потом манёвр",
        "Ключевое правило: выбирайте действие",
        "Типичная ошибка новичка — действовать по спешке",
        "по смыслу вопроса проверяется применение нормы",
        "правила категории B",
        "Базовое правило: действуйте",
        "Типичная ошибка новичка — выбирать ответ по интуиции",
    ]
    return any(bit in text for bit in generic_bits)


def is_sign_question(q):
    question = q["question_es"].lower()
    answer = correct_answer(q)["text_es"].lower()
    return (
        q.get("image")
        and (
            "señal" in question
            or "señales" in question
            or "demarcación" in question
            or "panel" in answer
            or q["id"] >= "category-b-0407"
        )
    )


def clean_answer_ru(q):
    ans = correct_answer(q)
    es = ans["text_es"].strip().rstrip(".")
    ru = ans.get("text_ru", "").strip().rstrip(".")
    if es.lower().startswith("verdadero"):
        return "утверждение верно"
    if es.lower().startswith("falso"):
        return "утверждение неверно"
    if es.startswith("Opción "):
        return f"вариант {es[-1]}"
    if es.startswith("Imagen "):
        return f"изображение {es[-1]}"
    if ru:
        return ru
    return es


def clean_old_why(q):
    text = norm_space(q.get("whyCorrect_ru", ""))
    if not text or is_generic(text):
        return ""
    text = re.sub(r"^Правильный ответ\s*—\s*[^:,.]+[:,]?\s*потому что\s*", "", text)
    text = re.sub(r"^Верный ответ\s*(?:[A-C]|Verdadero|Falso)?[:,]?\s*", "", text)
    text = re.sub(r"^Правильный вариант\s*(?:[A-C])?[:,]?\s*", "", text)
    text = text.replace("Экзамен проверяет, ", "Важно понять: ")
    return text


