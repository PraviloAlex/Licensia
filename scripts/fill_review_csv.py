def should_replace_explanation(row):
    return is_generic(row.get("explanation_ru_OLD", "")) or "category-b" in row.get("explanation_ru_OLD", "")


def should_replace_why(row, q):
    old = row.get("whyCorrect_ru_OLD", "")
    return is_generic(old) or (is_sign_question(q) and "пиктограм" in old)


def should_replace_common(row):
    return is_generic(row.get("commonMistake_ru_OLD", ""))


def should_replace_key(row):
    return is_generic(row.get("keyRule_ru_OLD", ""))


def should_replace_hint(row):
    old = row.get("memoryHint_ru_OLD", "")
    return is_generic(old) or old.strip() in {"Сначала безопасность, потом скорость и манёвр.", "Сначала безопасность, потом скорость и маневр."}


def final_value(row, old_col, generated, force_generated=False):
    old = row.get(old_col, "")
    if force_generated or not old.strip() or is_generic(old):
        return generated
    return old


def run(output_path: Path, full: bool = False):
    questions = {q["id"]: q for q in json.loads(QUESTIONS_JSON.read_text(encoding="utf-8"))}
    output_path.parent.mkdir(parents=True, exist_ok=True)

    input_path = CSV_IN if CSV_IN.exists() else CSV_OUT
    with input_path.open("r", encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = reader.fieldnames
        rows = list(reader)

    changed_counts = {
        "explanation_ru_NEW": 0,
        "whyCorrect_ru_NEW": 0,
        "commonMistake_ru_NEW": 0,
        "keyRule_ru_NEW": 0,
        "memoryHint_ru_NEW": 0,
    }

    missing_images = []
    for row in rows:
        q = questions[row["id"]]
        explanation, why, mistake, key, hint = templates_for(q)

        if q.get("image"):
            image_path = ROOT / "public" / q["image"]["src"]
            if not image_path.exists():
                row["explanation_ru_NEW"] = "⚠️ ПРОВЕРИТЬ: вопрос требует просмотра изображения"
                missing_images.append(row["id"])
                changed_counts["explanation_ru_NEW"] += 1
                continue

        if full:
            row["explanation_ru_NEW"] = final_value(row, "explanation_ru_OLD", explanation, force_generated=should_replace_explanation(row))
            row["whyCorrect_ru_NEW"] = final_value(row, "whyCorrect_ru_OLD", why, force_generated=should_replace_why(row, q))
            row["commonMistake_ru_NEW"] = final_value(row, "commonMistake_ru_OLD", mistake, force_generated=should_replace_common(row))
            row["keyRule_ru_NEW"] = final_value(row, "keyRule_ru_OLD", key, force_generated=should_replace_key(row))
            row["memoryHint_ru_NEW"] = final_value(row, "memoryHint_ru_OLD", hint, force_generated=should_replace_hint(row))
            for col in changed_counts:
                if row[col].strip():
                    changed_counts[col] += 1
            continue

        if should_replace_explanation(row):
            row["explanation_ru_NEW"] = explanation
            changed_counts["explanation_ru_NEW"] += 1
        if should_replace_why(row, q):
            row["whyCorrect_ru_NEW"] = why
            changed_counts["whyCorrect_ru_NEW"] += 1
        if should_replace_common(row):
            row["commonMistake_ru_NEW"] = mistake
            changed_counts["commonMistake_ru_NEW"] += 1
        if should_replace_key(row):
            row["keyRule_ru_NEW"] = key
            changed_counts["keyRule_ru_NEW"] += 1
        if should_replace_hint(row):
            row["memoryHint_ru_NEW"] = hint
            changed_counts["memoryHint_ru_NEW"] += 1

    with output_path.open("w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, quoting=csv.QUOTE_ALL)
        writer.writeheader()
        writer.writerows(rows)

    print(f"wrote={output_path}")
    print(f"rows={len(rows)}")
    print(f"missing_images={len(missing_images)}")
    for key, value in changed_counts.items():
        print(f"{key}={value}")


def main():
    run(CSV_OUT)
    run(CSV_OUT_FULL, full=True)


if __name__ == "__main__":
    main()
