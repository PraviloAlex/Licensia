def main():
    questions = {q["id"]: q for q in json.loads(QUESTIONS_JSON.read_text(encoding="utf-8"))}
    with CSV_IN.open("r", encoding="utf-8-sig", newline="") as f:
        rows = list(csv.DictReader(f))
        fieldnames = f.seek(0) or csv.DictReader(f).fieldnames

    updated = 0
    for row in rows:
        q = questions[row["id"]]
        if q.get("image"):
            continue
        ex, why, mistake, key, hint = map(clean, make_no_image_text(q))
        row["explanation_ru_NEW"] = ex
        row["whyCorrect_ru_NEW"] = why
        row["commonMistake_ru_NEW"] = mistake
        row["keyRule_ru_NEW"] = key
        row["memoryHint_ru_NEW"] = hint
        updated += 1

    CSV_OUT.parent.mkdir(parents=True, exist_ok=True)
    with CSV_OUT.open("w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, quoting=csv.QUOTE_ALL)
        writer.writeheader()
        writer.writerows(rows)

    print(f"wrote={CSV_OUT}")
    print(f"updated_no_image={updated}")


if __name__ == "__main__":
    main()
