def ans(q):
    return next(o for o in q["options"] if o["id"] == q["correctOptionId"])


def letter(q):
    return q["correctOptionId"].upper()


def ru_answer(q):
    a = ans(q)
    es = a["text_es"].strip().rstrip(".")
    ru = a.get("text_ru", "").strip().rstrip(".")
    if es.lower().startswith("verdadero"):
        return "утверждение верно"
    if es.lower().startswith("falso"):
        return "утверждение неверно"
    if ru:
        return ru
    return es


def short_answer(q):
    text = ru_answer(q)
    text = text.replace("Оба ответа А и Б верны", "оба варианта A и B верны")
    return text


def clean(text):
    return re.sub(r"\s+", " ", text).strip()


