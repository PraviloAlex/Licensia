def main():
    with CSV_IN.open("r", encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = reader.fieldnames
        rows = list(reader)

    changed = 0
    for row in rows:
        patch = PATCHES.get(row["id"])
        if not patch:
            continue
        (
            row["explanation_ru_NEW"],
            row["whyCorrect_ru_NEW"],
            row["commonMistake_ru_NEW"],
            row["keyRule_ru_NEW"],
            row["memoryHint_ru_NEW"],
        ) = patch
        changed += 1

    with CSV_OUT.open("w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, quoting=csv.QUOTE_ALL)
        writer.writeheader()
        writer.writerows(rows)

    print(f"wrote={CSV_OUT}")
    print(f"changed={changed}")
    print("suspicious_manual_review:")
    for key, value in SUSPICIOUS.items():
        print(f"{key}: {value}")


if __name__ == "__main__":
    main()
